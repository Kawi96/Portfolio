        <h1 id="experience_tableau_de_compta">Les synthèse du tableau de comptabilité d’AD Ingé</h1>
       
            <h2>Projet :</h2>
                <p>Fabrice Emeriau m’a demandé de réalisé des synthèse  synthèse pour décomposer et faciliter l’analyse du chiffre d’affaire de l’entreprise.</p>

        
            <h2>Objectif :</h2>
                <p>Il fallait que ces tableaux soit lisibles et clairs. Les synthèses devaient être developpées directement sur le tableau de comptabilité de l'entreprise pour être pouvoir suivre son évolution (je devais donc être vigilant car une fausse manipulation aurait pu provoquer des erreurs dans la comptabilité de l'entreprise).</p>
                <p>Les objectifs ont évolués au fur et à mesure de l’avancée du projet en fonction des demande de Fabrice Emeriau et des ingénieurs : Il fallait pouvoir effectuer des tri par type de clients, type de missions effectué, zone géographique, montant du chiffre d’affaire de la l’opération...</p> 

        
            <h2>Approche :</h2>
                <p>La méthode travail était totalement empirique et progressive : Je me suis formé aux fonctionnalités d’Excel nécessaires pour la réalisation de ce travail.</p>
                <p>J’ai amélioré progressivement les algorithms insérés dans les cellules des synthèses au gré des demandes. Au final la taille de l’algorithme entré dans chaque cellule représentait environ ⅔ de la taille d’une page Word.</p>
                <p>Les algorithmes en question se composent de séries d'équations du type "Somme SI ENS". Le tout inséré dans des formules "SI ESTVIDE" pour pouvoir éffectuer un tri supplémentaire par type de client (via la case en jaune au dessus du tableau qui comprend une liste déroulante) et permettre d'avoir également accès au résultat du total de tous les client (quand on sélectionne l'espace blanc inclue dans la liste déroulante de la case en jaune). CF : image ci-dessous :</p>
                <img class="image_centrale"  src="images/algorithme_tableau_compta.png" name="algorithme_tabeau_compta" alt="Image représentant l'ensemble des équation du tableau de compta">

        
            <h2>Durée :</h2>
                <p>Les synthèses du tableau de compta ont été developpé sur une période d’environ 6 mois durant laquelle je consacrais quelques heures par semaines à ce travail en parrallèles des autres tâches inhérantes à mon poste. Le total du temps passé sur ce travail est de quelques dizaines d’heures.</p>

        
            <h2>Mon Rôle :</h2>
                <p>J’ai réalisé les synthèses de A à Z en fonction des demandes de Fabrice Emeriau et parfois d'autres ingénieurs de l'entreprise</p>
        
