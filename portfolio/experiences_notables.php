                <p>Le marché en question concernait la démolition d’un ensemble immobilier appartenant à ENGIE à proximité du stade de France à St Denis (93). A l’emplacement de cet ensemble immobilier devait être construit le Centre Aquatique Olympique, bâtiment phare des JO de Paris 2024.</p>
