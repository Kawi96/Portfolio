        <h1 id="experience_enquete_asperansa">Les enquête sociologiques les besoins des adultes autistes</h1>
        
            <h2>Projet :</h2>
                <p>Dans le cadre de la préparation du 4ime plan Autisme, Ségolène Neuville, secrétaire d’état au handicap de 2014 à 2017 a cherché à recueillir l’avis de personnes directement impactés par ce handicap. Auparavant les seules données recueillis proviennent de parents d’enfants autistes et de quelques témoignages individuels. J’ai été à l’initiative de deux enquêtes sociologiques ayant pour thème “Les besoins des adultes autistes”. La première était une enquête qualitative sur 20 personnes réalisé par moi même (Des membres de l’association Aspéransa ont ensuite fortement contribué à la publication de l'enquête). Suite aux résultats de cette enquête, j’ai été à l’initiative d’une seconde enquête quantitative. Cette enquête a été coréalisé par plusieurs membres de l’association Aspéransa (Jean Vinçot, Hadrien Lehre et moi même) avec l’aide d’Abigael Jean qui était à l’époque stagiaire au seing de l’association.</p>

        
            <h2>Objectif :</h2>
                <p>L’objectif de ces enquêtes étaient d’obtenir un maximum de données concernant les besoins des personnes autistes adultes afin que les mesures prises par les pouvoirs publics soient corrélés avec les besoins des principaux intéressés.</p>
        
            <h2>Approche :</h2>
                <p>Pour réaliser l'enquête qualitative, j’ai directement été interviewer des personnes concernées à Paris, Lorient et Brest. Les contacts avec les personnes interviewées ont été établies à la suite de posts dédiés sur le forum de l’association Aspéransa.</p>
                <p>L’enquete quantitative a été publiée sur le site sondageonligne.com par Abigael Jean et le lien a été largement diffusé auprès des associations d’usagers et de parents d’enfants autistes.</p>
        
            <h2>Durée :</h2>
                <p>L'enquête qualitative a été durant 3 weekends du mois de Septembre 2016.</p>
                <p>Le lien quantitative vers l'enquête quantitative est resté en ligne durant ans (d’avril 2017 à avril 2018). Une synthèse a été faite au bout de deux mois de publication pour être versé à la documentation pour être utilisée comme ressource dans le cadre du travail sur sur quatrième plan autisme.</p>
        
            <h2>Mon Rôle :</h2>
                <p><span class="element_en_gras">Concernant l'enquête qualitative :</span> J’ai réalisé le questionnaire de l'enquête ainsi que les 20 interviews. J’ai ensuite participé au traitement des résultats avec l’aide deux autres membres d’Aspéransa (Hadrien Lehre et Jean Vinçot).</p>
                <p><span class="element_en_gras">Concernant l'enquête quantitative:</span> Je suis à l’initiative de cette enquête (proposition faite au Conseil d’Administration d’Aspéransa lors du constat que le nombre de personnes interviewé lors de la première enquête était trop faible pour pesé auprès des instances nationales). J’ai ensuite fait partie du groupe qui l’a coréalisé (avec Jean Vinçot, Hadrien Lehre et Abigael Jean). Je n’ai en revanche pas participé à l’analyse et à la rédaction de l'enquête.</p>

        
            <h2>Résultat :</h2>
                <div class="happy_unhappy">
                    <img class="image" src="images/pouce_vert.webp" name="top" alt="Main de couleur verte avec un pouce en l'air">
                    <p class="texte_happy_unhappy"><span class=element_en_gras>L'enquête qualitative</span> a été utilisée par le cabinet de Ségolène Neuville dans le cadre du 4ieme plan autisme.<span class=element_en_gras>L’enquete quantitative</span> était la première enquête en terme de participants concernant les besoins des adultes autistes. Elle a été utilisée comme annexe par le cabinet de Sophie Cluzel dans le cadre de la préparation du 4ieme plan autisme (qui faisait suite aux travaux inachevé du cabinet de Ségolène Neuville pour cause d’alternance politique).</p>
